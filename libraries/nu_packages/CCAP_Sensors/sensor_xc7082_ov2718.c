static rt_err_t xc7082_feed(struct rt_i2c_bus_device *i2cdev, sensor_priv *pdev, const sensor_reg_val *psSensorRegVal, int i32len)
{
    int i;
    uint8_t au8TxData[4];

    // BypassOn
    for (i = 0; i < i32len; i++)
    {
        const sensor_reg_val *psRegVal = &psSensorRegVal[i];

        au8TxData[0] = (uint8_t)((psRegVal->u16Addr >> 8) & 0x00FF); //addr [15:8]
        au8TxData[1] = (uint8_t)((psRegVal->u16Addr) & 0x00FF); //addr [ 7:0]
        au8TxData[pdev->u16AddrBL] = (uint8_t)((psRegVal->u16Val) & 0x00FF);  //data [ 7:0]

        if (ccap_sensor_i2c_write(i2cdev, pdev->u16DevAddr, (rt_uint8_t *)&au8TxData[0], pdev->u16AddrBL + pdev->u16ValBL) != RT_EOK)
        {
            LOG_E("[Failed] addr=0x%04x, data=0x%02x\n", psRegVal->u16Addr, psRegVal->u16Val);
            return -RT_ERROR;
        }
    }
    return RT_EOK;
}

static rt_err_t sensor_feed(struct rt_i2c_bus_device *i2cdev, sensor_priv *pdev, const sensor_reg_val *psSensorRegVal, int i32len)
{
    int i;
    uint8_t au8TxData[4];

    // BypassOn
    for (i = 0; i < i32len; i++)
    {
        const sensor_reg_val *psRegVal = &psSensorRegVal[i];

        au8TxData[0] = (uint8_t)((psRegVal->u16Addr >> 8) & 0x00FF); //addr [15:8]
        au8TxData[1] = (uint8_t)((psRegVal->u16Addr) & 0x00FF); //addr [ 7:0]
        au8TxData[pdev->u16AddrBL] = (uint8_t)((psRegVal->u16Val) & 0x00FF);  //data [ 7:0]

        if (ccap_sensor_i2c_write(i2cdev, DEF_SENSOR_ADDR, (rt_uint8_t *)&au8TxData[0], pdev->u16AddrBL + pdev->u16ValBL) != RT_EOK)
        {
            LOG_E("[Failed] addr=0x%04x, data=0x%02x\n", psRegVal->u16Addr, psRegVal->u16Val);
            return -RT_ERROR;
        }
    }
    return RT_EOK;
}


static rt_err_t xc7082_bypass_on(struct rt_i2c_bus_device *i2cdev, sensor_priv *pdev)
{
    return xc7082_feed(i2cdev, pdev, &s_sRegValue_XC7082_BypassOn[0], sizeof(s_sRegValue_XC7082_BypassOn) / sizeof(sensor_reg_val));
}

static rt_err_t xc7082_bypass_off(struct rt_i2c_bus_device *i2cdev, sensor_priv *pdev)
{
    return xc7082_feed(i2cdev, pdev, &s_sRegValue_XC7082_BypassOff[0], sizeof(s_sRegValue_XC7082_BypassOff) / sizeof(sensor_reg_val));
}

static rt_err_t ccap_sensor_set_mode_xc7082(struct rt_i2c_bus_device *i2cdev, void *pvPriv, sensor_mode_info *psInfo)
{
    sensor_priv *pdev = (sensor_priv *)pvPriv;

    RT_ASSERT(i2cdev);
    RT_ASSERT(pdev);
    RT_ASSERT(psInfo);

    LOG_I("XC7082 Sensor ID:%d, Width:%d, Height:%d, FMT:%08x",
          pdev->eId,
          psInfo->sViewInfo.u32Width,
          psInfo->sViewInfo.u32Height,
          psInfo->sViewInfo.u32PixFmt);

    if (xc7082_bypass_off(i2cdev, pdev) != RT_EOK)
    {
        LOG_E("Failed to feed bypass off settings.\n");
        goto exit_ccap_sensor_set_mode_xc7082;
    }

    if (xc7082_feed(i2cdev, pdev, &psInfo->psRegArr[0], psInfo->u32RegArrSize) != RT_EOK)
    {
        LOG_E("Failed to feed xc7082 settings.\n");
        goto exit_ccap_sensor_set_mode_xc7082;
    }

    if (xc7082_bypass_on(i2cdev, pdev) != RT_EOK)
    {
        LOG_E("Failed to feed bypass on settings.\n");
        goto exit_ccap_sensor_set_mode_xc7082;
    }

    if (sensor_feed(i2cdev, pdev, &s_sRegValue_OV2718_1080P_YUV[0], sizeof(s_sRegValue_OV2718_1080P_YUV) / sizeof(sensor_reg_val)) != RT_EOK)
    {
        LOG_E("Failed to feed ov2817 settings.\n");
        goto exit_ccap_sensor_set_mode_xc7082;
    }

    if (xc7082_bypass_off(i2cdev, pdev) != RT_EOK)
    {
        LOG_E("Failed to feed bypass off settings.\n");
        goto exit_ccap_sensor_set_mode_xc7082;
    }

    return RT_EOK;

exit_ccap_sensor_set_mode_xc7082:

    return -RT_ERROR;
}

